package com.training.jan2022.day1;

public class SampleIfElse {
	public static void main(String[] args) {
		int i = 6;
		if (i == 5) {
			System.out.println("value of i is " + i);

		} else if (i == 6) {
			System.out.println("value of i is " + i);
		} else {
			System.out.println("i is neither 5 nor 6");
		}
	}

}
